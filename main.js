/* Grifinoria */ 
function adicionarPontoGrifinoria() {
    const placar = document.getElementById('placar-grifinoria');
    let pontuacao = parseInt(placar.textContent);
    pontuacao++;
    placar.textContent = pontuacao;
  }
  

  function subtrairPontoGrifinoria() {
    const placar = document.getElementById('placar-grifinoria');
    let pontuacao = parseInt(placar.textContent);
    pontuacao--;
    placar.textContent = pontuacao;
  }

